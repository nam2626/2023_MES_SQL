--테이블 생성 구문
--Person 테이블 생성
--	이름	문자열
--	나이	정수
CREATE TABLE PERSON(
	PNAME VARCHAR2(30),
	AGE NUMBER(3)
);

DROP TABLE PERSON;

--Member 테이블 생성
-- 아이디  
-- 비밀번호
-- 이름
-- 나이
-- 성별
CREATE TABLE MEMBER(
	MEMBER_ID VARCHAR2(20) PRIMARY KEY,
	MEMEBER_PASSWD VARCHAR2(70) NOT NULL,
	MEMBER_NAME VARCHAR2(30),
	MEMBER_AGE NUMBER(3) DEFAULT 1,
	MEMBER_GENDER CHAR(1)	
);

--학생 테이블
--학번, 이름, 학과명, 평점
CREATE TABLE STUDENT(
	STD_NO CHAR(8) PRIMARY KEY,
	STD_NAME VARCHAR2(30),
	STD_MAJOR VARCHAR2(60),
	STD_SCORE NUMBER(3,2)
);












